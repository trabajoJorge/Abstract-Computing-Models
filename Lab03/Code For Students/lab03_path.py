def graph_has_a_path(graph, start, end):

      nodosMarcados = []
      aProcesar = [] 
      aProcesar.append(start)       # Guardo en la lista el nodo actual desde el cual voy a partir para encontrar una ruta.

      while aProcesar:
            actual = aProcesar.pop(0)           # Proceso todos los nodos en orden de llegada, por lo que cojo el ultimo y lo elimino de la lista a procesar con pop
            
            if actual == end:                   # Caso trivial en el que si se llega al nodo final significa que ha encontrado una ruta y devuelve True.
                  nodosMarcados.append(actual)
                  return True

            if actual not in nodosMarcados:              # Si el nodo actual no esta en la lista de nodos visitado lo a√±ado 
                  nodosMarcados.append(actual)

            for i in range(0,len(graph)):
                  if graph[actual][i]==1 and i not in nodosMarcados:          # Cogemos todos los nodos conectados y que no esten en la ruta para posteriormente procesarlos 
                        aProcesar.append(i)                                   # y a traves de ellos encontrar una ruta.
      return False

      # Coste: O(n^2)

